void solve(){
	int n; cin>>n;
	unordered_map<int, int> mp;
	for(int i = 0; i< n; i++){
		int temp; cin>>temp;
		mp[temp]++;
	}
	int ans = 0; 
	for(auto x: mp){
		// cout<<x.first<<" "<<x.second<<endl;
		ans = max(ans, x.second);
	}
	// cout<<ans<<endl;

	if(ans == n){
		cout<<0<<endl;
	}
	else if(ans == 1){
		cout<<n-1<<endl;
	}
	else{
		cout<<n-ans<<endl;
	}


}

